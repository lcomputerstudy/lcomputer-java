package com.lcomputerstudy.books.java.ch3;

// 부정 연산자
public class Ex6NotOperator {
	public static void main(String[] args) {
		boolean result = true;
		System.out.printf("result = %b%n", result);
		System.out.printf("!result = %b%n", !result);
		System.out.printf("!!result = %b%n", !!result);
		System.out.printf("!!!result = %b%n", !!!result);
	}
}
